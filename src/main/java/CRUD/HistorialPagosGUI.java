    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked

        int fila = table.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro");
        } else {
            idc = Integer.parseInt((String) table.getValueAt(fila, 0).toString());
            String fecha = table.getValueAt(fila, 1).toString();
            String alumno = table.getValueAt(fila, 2).toString();
            String plan = table.getValueAt(fila, 3).toString();
            double monto = Double.parseDouble((String) table.getValueAt(fila, 4).toString());
            double descuento = Double.parseDouble((String) table.getValueAt(fila, 5).toString());
            double total = Double.parseDouble((String) table.getValueAt(fila, 6).toString());
            java.util.Date fecha2;

            try {
                fecha2 = new SimpleDateFormat("yyyy-MM-dd").parse(fecha);
                date.setDate(fecha2);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "revise los datos");
            }
            txtalumno.setText(alumno);
            cboplan.setSelectedItem(plan);
            txtmonto.setText(Double.toString(monto));
            txtdescuento.setText(Double.toString(descuento));
            txttotal.setText(Double.toString(total));

        }


    }//GEN-LAST:event_tableMouseClicked

