    void LimpiarTabla() {
        DefaultTableModel modelo = (DefaultTableModel) table.getModel();
        int rowCount = modelo.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            modelo.removeRow(i);
        }
    }

    private void consultar() {

        String sql = "SELECT * FROM pagos";

        try {
            conect = con.getConnection();
            st = conect.createStatement();
            rs = st.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            LimpiarTabla();
            while (rs.next()) {
                Object[] fila = new Object[7];  // Número de columnas en la tabla
                fila[0] = rs.getInt("id");
                fila[1] = rs.getString("fecha");
                fila[2] = rs.getString("alumno");
                fila[3] = rs.getString("plan");
                fila[4] = rs.getDouble("monto");
                fila[5] = rs.getDouble("descuento");
                fila[6] = rs.getDouble("total");
                model.addRow(fila);  // Añadir la fila al modelo
            }
            table.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al consultar los datos: " + e.getMessage());
        }
    }

    public void modificar(int id) {
        String fecha = ((JTextField) date.getDateEditor().getUiComponent()).getText();
        String alumno = txtalumno.getText();
        String plan = cboplan.getSelectedItem().toString();
        Double monto = Double.parseDouble(txtmonto.getText());
        Double descuento = Double.parseDouble(txtdescuento.getText());

        Double porcentaje = 0.13;
        Double iva = monto * porcentaje;
        Double totalcalcular = monto - descuento + iva;
        txttotal.setText(Double.toString(totalcalcular));
        Double total = Double.parseDouble(txttotal.getText());

        if (alumno.isEmpty() || plan.isEmpty() || fecha.isEmpty() || monto.isNaN()) {
            JOptionPane.showMessageDialog(null, "Ingrese todos los datos");
        } else {
            try {
                // SQL para actualizar el registro donde el ID coincide
                String sql = "UPDATE pagos SET fecha = ?, alumno = ?, plan = ?, monto = ?, descuento = ?, total = ? WHERE id = ?";
                Connection conect = con.getConnection();
                PreparedStatement pst = conect.prepareStatement(sql);
                pst.setString(1, fecha);
                pst.setString(2, alumno);
                pst.setString(3, plan);
                pst.setDouble(4, monto);
                pst.setDouble(5, descuento);
                pst.setDouble(6, total);
                pst.setInt(7, id);  // Asegúrate de pasar el ID del registro que deseas modificar
                pst.executeUpdate();
                System.out.println("Registro modificado con éxito");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error al modificar los datos en la base de datos: " + e.getMessage());
            }
        }
    }

    private void eliminar() {
        int fila = table.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "Seleccione un registro para eliminar");
        } else {
            idc = Integer.parseInt(table.getValueAt(fila, 0).toString());

            int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro de que desea eliminar este registro?", "Confirmar Eliminación", JOptionPane.YES_NO_OPTION);
            if (confirmacion == JOptionPane.YES_OPTION) {
                String sql = "DELETE FROM pagos WHERE id = ?";

                try {
                    conect = con.getConnection();
                    PreparedStatement pst = conect.prepareStatement(sql);
                    pst.setInt(1, idc);
                    pst.executeUpdate();

                    JOptionPane.showMessageDialog(null, "Registro eliminado con éxito");

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error al eliminar el registro: " + e.getMessage());
                }
            }
        }
    }

